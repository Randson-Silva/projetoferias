    private void btConsultarFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConsultarFuncionarioActionPerformed
        // TODO add your handling code here:
        String nome = txtNome.getText();

        Funcionario cli = new Funcionario();
        FuncionarioDAO dao = new FuncionarioDAO();

        cli = dao.consultarFuncionario(nome);

        if (cli.getNome() != null) {
            txtCodigo.setText(String.valueOf(cli.getId()));
            txtNome.setText(cli.getNome());
            txtRg.setText(cli.getRg());
            txtCpf.setText(cli.getCpf());
            txtEmail.setText(cli.getEmail());
            txtSenha.setText(cli.getSenha());
            txtCargo.setText(cli.getCargo());
            cbbAcesso.setSelectedItem(cli.getEstado());
            txtTelefone.setText(cli.getTelefone());
            txtCelular.setText(cli.getCelular());
            txtCep.setText(cli.getCep());
            txtEndereco.setText(cli.getEndereco());
            txtNumero.setText(String.valueOf(cli.getNumero()));
            txtComplemento.setText(cli.getComplemento());
            txtBairro.setText(cli.getBairro());
            txtCidade.setText(cli.getCidade());
            cbbEstado.setSelectedItem(cli.getEstado());
        } else {
            JOptionPane.showMessageDialog(null, "Funcionário não encontrado!",
                    "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btConsultarFuncionarioActionPerformed

