    private void btPesquisarNomesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarNomesActionPerformed
        // TODO add your handling code here:
        String nome = "%" + txtPesquisarNome.getText() + "%";

        FuncionarioDAO dao = new FuncionarioDAO();
        List<Funcionario> funcionarios = dao.pesquisarFuncionarioPorNome(nome);
        DefaultTableModel dados = (DefaultTableModel) tbFuncionarios.getModel();
        dados.setNumRows(0);

        for (Funcionario funcionario : funcionarios) {
            dados.addRow(new Object[]{
                funcionario.getId(),
                funcionario.getNome(),
                funcionario.getRg(),
                funcionario.getCpf(),
                funcionario.getEmail(),
                funcionario.getTelefone(),
                funcionario.getCelular(),
                funcionario.getCep(),
                funcionario.getEndereco(),
                funcionario.getNumero(),
                funcionario.getComplemento(),
                funcionario.getBairro(),
                funcionario.getCidade(),
                funcionario.getEstado()
            });
        }
    }//GEN-LAST:event_btPesquisarNomesActionPerformed

